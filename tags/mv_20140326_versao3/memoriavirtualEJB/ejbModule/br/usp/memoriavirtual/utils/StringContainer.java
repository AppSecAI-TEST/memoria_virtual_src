package br.usp.memoriavirtual.utils;

public class StringContainer {
	
	private String valor;
	
	public StringContainer(){
		
	}

	public StringContainer(String s){
		this.setValor(s);
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}
	
}
